GO NOTES

"fmt" package   // format package for formatting strings and numbers in Go

go run main.go  // run the main.go file

main package    // main package is the entry point of the program

func main() {}   // main function is the entry point of the program


var // to declare a variable
const // to declare a constant    ------> not changeable


printf() // to print a formatted string
%v // to print the value of a variable
%T // to print the type of a variable
%#v // to print the value of a variable in Go syntax
%t // to print the boolean value of a variable
%d // to print the integer value of a variable
%f // to print the float value of a variable
%e // to print the scientific notation of a variable
%s // to print the string value of a variable
%x // to print the hexadecimal value of a variable
%o // to print the octal value of a variable
%b // to print the binary value of a variable
%q // to print the quoted string value of a variable
%U // to print the Unicode value of a variable
%#U // to print the Unicode value of a variable in Go syntax
%#x // to print the Unicode value of a variable in hexadecimal
%#o // to print the Unicode value of a variable in octal
%#q // to print the Unicode value of a variable in quoted string



