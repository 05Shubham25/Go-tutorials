/* 
2. System-Level Understanding
At a system level, complexity analysis helps you understand:

CPU cycles → Time complexity.

Memory usage (RAM) → Space complexity.

✅ Example:

An algorithm that loops through a list:

Time complexity: Number of operations = Number of list elements.

Space complexity: Memory used by the list + temporary variables.

*/


/*
 
3. Time Complexity Explained
✅ Time Complexity measures the number of operations based on the input size n.
It is expressed using Big-O Notation → O(f(n)), where:

O: Upper bound of the time complexity.

f(n): A function representing the operations performed based on input size n.

*/